import i18n from "i18next";
import { initReactI18next } from "react-i18next";
import LanguageDetector from "i18next-browser-languagedetector";
import en from "./locales/en";
import ar from "./locales/ar";

i18n
  .use(LanguageDetector)
  .use(initReactI18next)
  .init({
    resources: {
      en: { translation: en },
      ar: { translation: ar },
    },
    fallbackLng: "en",
    interpolation: {
      escapeValue: false,
    },
    detection: {
      order: ["localStorage", "navigator"],
      caches: ["localStorage"],
    },
  });

// Setup dynamic HTML document directions
i18n.on("languageChanged", (lng) => {
  document.documentElement.dir = lng === "ar" ? "rtl" : "ltr";
  document.documentElement.lang = lng;
});

// Run initially
const initialLanguage = i18n.language || "en";
const cleanLang = initialLanguage.startsWith("ar") ? "ar" : "en";
document.documentElement.dir = cleanLang === "ar" ? "rtl" : "ltr";
document.documentElement.lang = cleanLang;

export default i18n;
